public class LibreriaPromedio {
    
    //calcular promedio para 2 números
    public static double calcularPromedio(double num1, double num2){
        return (num1 + num2) / 2;
    }
    
    //calcular promedio para 3 números
    public static double calcularPromedio(double num1, double num2, double num3){
        return (num1 + num2 + num3) / 3;
    }
    
    //calcular promedio para 4 números
    public static double calcularPromedio(double num1, double num2, double num3, double num4){
        return (num1 + num2 + num3 + num4) / 4;
    }
    
    //calcular promedio para 5 números
    public static double calcularPromedio(double num1, double num2, double num3, double num4, double num5){
        return (num1 + num2 + num3 + num4 + num5) / 5;
    }
}
